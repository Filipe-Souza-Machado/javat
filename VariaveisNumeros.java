public class VariaveisNumeros {
    public static void main(String[] args) {

        double a = 6;
        double b = 6;
        double soma = a + b;
        double SUBT = a - b;
        double MULT = a * b;
        double DIV = a / b;

         System.out.println(a+" + " +b+ " = "+soma);
         System.out.println(a+" - " +b+ " = "+SUBT);
         System.out.println(a+" x " +b+ " = "+MULT);
         System.out.println(a+" : " +b+ " = "+DIV);
    }
}
